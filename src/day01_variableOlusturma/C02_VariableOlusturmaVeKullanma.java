package day01_variableOlusturma;

public class C02_VariableOlusturmaVeKullanma {

    public static void main(String[] args) {


        int sayi = 20 ;

        System.out.println("sayi");

        System.out.println(sayi);

        sayi = 30 ;

        System.out.println(sayi);

        sayi = 50 ;

        System.out.println(sayi);


        sayi = sayi + 20;

        System.out.println(sayi);


        sayi = (sayi - 10) / 2;

        System.out.println(sayi);
    }
}
