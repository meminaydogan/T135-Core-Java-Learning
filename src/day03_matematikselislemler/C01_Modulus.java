package day03_matematikselislemler;

public class C01_Modulus {
    public static void main(String[] args) {

        System.out.println( 20 % 3);
        System.out.println( 45 % 5); // sayi 5 ile tam bölünüyor.
        System.out.println( 23 % 2); // sayıların tek çift sayı olduğunu kontrolde kullanılır
        System.out.println( 245 % 10); //birler basamağını bulmak için

        System.out.println(245 / 10); // birler basamağından kurtulmak için kullanılır.
    }
}
